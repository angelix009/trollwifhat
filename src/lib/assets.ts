// Helper functions
export const getAssetPath = (category: keyof typeof ASSETS, filename: string | null) => {
  if (!filename) return null;
  return `/assets/${category}/${filename}`;
};

export const getTrollFace = (id: string) => {
  return ASSETS.trollfaces.find(face => face.id === id);
};

export const getHat = (id: string) => {
  return ASSETS.hats.find(hat => hat.id === id);
};

export const getBackground = (id: string) => {
  return ASSETS.backgrounds.find(bg => bg.id === id);
};